upload() {
  local file="$1"
  local slug="$2"
  local label="$3"
  COUNT=$((COUNT + 1))
  echo "[$COUNT/$TOTAL] $label..." >&2
  RESP=$(npx vercel curl "$PREVIEW/api/upload-temp" -s -X POST \
    -H "x-upload-secret: $SECRET" \
    -F "file=@$file" \
    -F "slug=$slug" \
    -F "business_id=$BIZ" 2>/dev/null | grep -o '{.*}')
  if [ -z "$RESP" ]; then
    echo "  FAIL" >&2
    return
  fi
  echo "  OK" >&2
  if [ "$FIRST" = true ]; then
    FIRST=false
  else
    echo "," >> "$RESULTS"
  fi
  echo "$RESP" >> "$RESULTS"
}

